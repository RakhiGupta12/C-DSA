#include<iostream>
using namespace std;

int main(){

    int a = 4; 
    int b = 8;
    cout<<(a&b)<<endl;
    cout<<(a|b)<<endl;
    cout<<(a^b)<<endl;
    // h/w
    cout<<(6&10)<<endl;
    cout<<(6|10)<<endl;
    cout<<(6^10)<<endl;
    // left shift (<<)
    cout<<(4<<1)<<endl;
    cout<<(10<<2)<<endl;

    // right shift
    cout<<(10>>1)<<endl;
    cout<<(8<<2)<<endl;
    cout<<(10>>1)<<endl;

    return  0;
}